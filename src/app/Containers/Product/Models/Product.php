class Product extends Model
{
    protected $fillable = [
        "name",
        "description",
        "cost",
    ];
    protected $casts=[

    ];

    public function getName(): string
    {
        return $this->name;
    }

    public function setName(string $name): void
    {
        $this->name = $name;
    }

    public function getDescription(): ?string
    {
        return $this->description;
    }

